export interface Cruise {
  id: string;
  name: string;
  subtitle: string;
  description: string;
  images: string[];
  price: number;
  priceLabel: string;
  route: string;
  duration: string;
  capacity: string;
  cabins: string;
  facilities: string[];
  packages: CruisePackage[];
  seatPlan: Deck[];
  seatPlanImage?: string;
  menuImage?: string;
  featured: boolean;
  itinerary?: ItineraryDay[];
  menu?: MenuDay[];
  safetyInfo?: string[];
  travelTips?: string[];
  thingsToCarry?: string[];
  packageIncludes?: string[];
  touristSpots?: string[];
  additionalCosts?: { label: string; amount: string }[];
}

export interface ItineraryDay {
  day: string;
  title: string;
  activities: string[];
}

export interface MenuDay {
  day: string;
  meals: { name: string; items: string }[];
}

export interface CruisePackage {
  id: string;
  name: string;
  route: string;
  price: number;
  inclusions: string[];
  seatType: 'AC' | 'Non-AC' | 'Both';
}

export interface Deck {
  name: string;
  capacity: number;
  rows: CabinRow[];
}

export interface CabinRow {
  label: string;
  cabins: Cabin[];
}

export interface Cabin {
  id: string;
  type: 'VIP Couple' | 'VIP Family' | 'Twin' | 'Single' | 'Bunk';
  persons: number;
  available: boolean;
  bedType: string;
}

export interface Booking {
  id: string;
  name: string;
  phone: string;
  cruiseName: string;
  travelDate: string;
  persons: number;
  packageName: string;
  seatType: string;
  createdAt: string;
}

export interface Testimonial {
  id: string;
  name: string;
  text: string;
  rating: number;
  avatar: string;
}

export interface TeamMember {
  name: string;
  role: string;
  image: string;
}

